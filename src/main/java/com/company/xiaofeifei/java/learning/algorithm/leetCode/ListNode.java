package com.company.xiaofeifei.java.learning.algorithm.leetCode;

/**
 * 链表
 * @author zouzy
 * @date 2020-10-09
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }
    ListNode(int val) {
        this.val = val;
        this.next = null;
    }
    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
    
}



