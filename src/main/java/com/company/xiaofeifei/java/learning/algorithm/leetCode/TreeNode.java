package com.company.xiaofeifei.java.learning.algorithm.leetCode;

/**
 * 二叉树
 * @author zouzy
 * @date 2020-09-30
 */
public class TreeNode {
    int val;
    TreeNode left;
    TreeNode right;

    TreeNode() {
    }
    TreeNode(int val) {
        this.val = val;
    }
    TreeNode(int val, TreeNode left, TreeNode right) {
        this.val = val;
        this.left = left;
        this.right = right;
    }

}



